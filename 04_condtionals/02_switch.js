//switch used for checking from multiple conditions
// switch (key) { key is the value for which we have to check each time.
//     case value:
        
//         break;

//     default: // if nothing matches then default will be executed
//         break;
// } defualt syntax for switch

let month=3
switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("MArch");
        break;
    case 4:
        console.log("Apr");
        break;
    
    

    default:
        console.log("XYZ");
        
        break;
}